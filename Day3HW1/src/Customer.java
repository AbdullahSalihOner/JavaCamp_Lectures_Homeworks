
public class Customer {
	
	//Constructor
	
	public Customer() 
	{
		System.out.println(" Customer Object Started");
	}
	
	
	//Variables
	public int Id;
	public String City;
	
	
	//Getter Setters
	
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getCity() {
		return City;
	}
	public void setCity(String city) {
		City = city;
	}
	


}
